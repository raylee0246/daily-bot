def get_weekly_trending(count=6):
    last_week = (datetime.now() - timedelta(days=7)).strftime('%Y-%m-%d')
    url = f"https://api.github.com/search/repositories?q=created:>{last_week}&sort=stars&order=desc"
    
    try:
        headers = {'User-Agent': 'Python Bot'}
        response = requests.get(url, headers=headers)
        data = response.json()
        
        results = []
        
        if 'items' in data and len(data['items']) > 0:
            pool_size = min(len(data['items']), 50)
            sample_size = min(pool_size, count)
            selected_repos = random.sample(data['items'][:pool_size], sample_size)
            
            for repo in selected_repos:
                original_desc = repo['description'] if repo['description'] else "ç„¡ç°¡ä»‹"
                
                try:
                    translated_desc = GoogleTranslator(source='auto', target='zh-TW').translate(original_desc)
                except Exception:
                    translated_desc = original_desc
                
                if len(translated_desc) > 100:
                    translated_desc = translated_desc[:97] + "..."

                # --- é€™è£¡ä¿®æ”¹äº†é‚è¼¯ï¼šæŠ“å– Topics (æ¨™ç±¤) ---
                topics = repo.get('topics', [])
                if topics:
                    # å¦‚æœæœ‰æ¨™ç±¤ï¼Œåªå–å‰ 3 å€‹ (å¤ªé•·æœƒå¾ˆé†œ)ï¼Œä¸¦ç”¨é€—è™Ÿé€£æ¥
                    # ä¾‹å¦‚: "react, dashboard, admin"
                    tags_display = ", ".join(topics[:3])
                else:
                    # å¦‚æœé–‹ç™¼è€…æ‡¶æƒ°æ²’å¯«æ¨™ç±¤ï¼Œæˆ‘å€‘æ‰é€€è€Œæ±‚å…¶æ¬¡é¡¯ç¤ºèªè¨€
                    tags_display = repo['language'] if repo['language'] else "é€šç”¨å·¥å…·"

                results.append({
                    "name": repo['name'],
                    "full_name": repo['full_name'],
                    "desc": translated_desc,
                    "tags": tags_display, # æŠŠåŸæœ¬çš„ language æ”¹æˆ tags
                    "stars": f"{repo['stargazers_count']:,}",
                    "link": repo['html_url']
                })
                
            return results
        return []
    except Exception as e:
        print(f"GitHub API Error: {e}")
        return []

